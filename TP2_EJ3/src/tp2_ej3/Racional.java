public class Racional {
    
    private int num;
    private int den;

    public Racional() {
        
        
        
    }

    public Racional(int num, int den) {
        
        this.num = num;
        this.den = den;
        
    }

    public int getNum() {
        
        return num;
        
    }

    public int getDen() {
        
        return den;
        
    }

    public void setNum(int num) {
        
        this.num = num;
        
    }

    public void setDen(int den) {
        
        this.den = den;
        
    }
    
    
    
}
